#include "libft.h"

int		ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*s3;
	unsigned char	*s4;
	size_t			i;

	if (s1 == s2 || n == 0)
		return (0);
	s3 = (unsigned char *)s1;
	s4 = (unsigned char *)s2;
	i = 0;
	while (i < n)
	{
		if (s3[i] == s4[i])
			i++;
		else
			return (s3[i] - s4[i]);
	}
	return (0);
}
