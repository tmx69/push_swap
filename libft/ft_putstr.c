#include "libft.h"

int		ft_putstr(char const *s)
{
	char	*s2;
	int		i;

	i = 0;
	if (s)
	{
		s2 = (char *)s;
		while (s2[i])
		{
			ft_putchar(s2[i]);
			i++;
		}
	}
	return (i);
}
